#include <iostream>
using namespace std;
int main() {
int age;
cout <<"Enter your age: ";
    cin >> age;
    if (age <= 12 ) {
        cout <<"You are an " << " Child";
    } else if (age > 12 && age <= 19 ) {
        cout <<"You are an " << " Teenager";

    } else if (age > 19 && age <= 59 ) {
        cout <<"You are an " << " Adult";

    } else {
        cout <<"You are an " << " Senior";

    }



    return 0;
}